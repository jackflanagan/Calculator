package Calculator.Calculator;

public class Adder {

	public int add(int fnum, int snum) {
		int sum = fnum + snum;
		return sum;
	}

	public int subtract(int fnum, int snum) {
		
		int sum = fnum - snum;
		return sum;
	}

	public int multiply(int fnum, int snum) {
		int sum = fnum * snum;
		return sum ;
	}

	public double divide(double fnum, double snum) {
		double sum = fnum / snum;
		return sum;
	}

}
